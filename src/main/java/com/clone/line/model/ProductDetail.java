package com.clone.line.model;

public class ProductDetail {
	String pModel;
	String pNum;
	String pStatus;
	String pCorp;
	String pBrand;
	String pOrigin;
	String pMdate;
	String thumbnail;
	String detail;
	String pPrice;
	public String getpModel() {
		return pModel;
	}
	public void setpModel(String pModel) {
		this.pModel = pModel;
	}
	public String getpNum() {
		return pNum;
	}
	public void setpNum(String pNum) {
		this.pNum = pNum;
	}
	public String getpStatus() {
		return pStatus;
	}
	public void setpStatus(String pStatus) {
		this.pStatus = pStatus;
	}
	public String getpCorp() {
		return pCorp;
	}
	public void setpCorp(String pCorp) {
		this.pCorp = pCorp;
	}
	public String getpBrand() {
		return pBrand;
	}
	public void setpBrand(String pBrand) {
		this.pBrand = pBrand;
	}
	public String getpOrigin() {
		return pOrigin;
	}
	public void setpOrigin(String pOrigin) {
		this.pOrigin = pOrigin;
	}
	public String getpMdate() {
		return pMdate;
	}
	public void setpMdate(String pMdate) {
		this.pMdate = pMdate;
	}
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getpPrice() {
		return pPrice;
	}
	public void setpPrice(String pPrice) {
		this.pPrice = pPrice;
	}
	
}
